import React, { useEffect, useRef } from 'react';
import { useSelector } from 'react-redux';
import { useNavigate } from 'react-router-dom';
import qs from 'qs';

import { sortTypes } from '../Components/Sort';
import { useAppDispatch } from '../redux/store';
import { selectFilters } from '../redux/filter/selectors';
import { setFilters } from '../redux/filter/slice';
import {
	fetchPizzas,
	getFilteredItemsCount,
} from '../redux/pizza/asyncActions';
import { selectPizzas } from '../redux/pizza/selectors';

import {
	Categories,
	Sort,
	PizzaBlock,
	Skeleton,
	Pagination,
} from '../Components';

const Home: React.FC = () => {
	const { items, status } = useSelector(selectPizzas);

	const isMounted = useRef(false);
	const isSearch = useRef(false);

	const navigate = useNavigate();
	const dispatch = useAppDispatch();

	const itemsPerPage = 8;

	const { sort, categoryId, searchValue, currentPage } =
		useSelector(selectFilters);

	// –ï—Å–ª–∏ –µ—Å—Ç—å –ø–∞—Ä–∞–º–µ—Ç—Ä—ã –≤ —Å—Ç—Ä–æ–∫–µ - –æ—Ç–ø—Ä–∞–≤–ª—è–µ—Ç –∏—Ö –≤ —Ä–µ–¥–∞–∫—Å
	useEffect(() => {
		if (window.location.search) {
			const params = qs.parse(window.location.search.substring(1));
			const sort = sortTypes.find(
				(sortType) => sortType.sortProperty === params.sortProperty
			);
			dispatch(
				setFilters({
					currentPage: Number(params.currentPage),
					categoryId: Number(params.categoryId),
					searchValue: String(params.search),
					sort: sort || sortTypes[0],
				})
			);
			isSearch.current = true;
		}
	}, [dispatch]);

	//
	useEffect(() => {
		const getPizzas = async () => {
			const category = categoryId > 0 ? `category=${categoryId}` : '';
			const sortBy = sort.sortProperty.replace('-', '');
			const order = sort.sortProperty.includes('-') ? 'asc' : 'desc';
			const search = searchValue ? `&search=${searchValue}` : '';
			dispatch(
				fetchPizzas({
					category,
					sortBy,
					order,
					search,
					currentPage: String(currentPage),
				})
			);
			dispatch(
				getFilteredItemsCount({
					category,
					sortBy,
					order,
					search,
					currentPage: String(currentPage),
				})
			);
		};
		getPizzas();
	}, [sort, categoryId, searchValue, currentPage, dispatch]);

	// –û—Ç–ø—Ä–∞–≤–ª—è–µ—Ç –ø–∞—Ä–∞–º–µ—Ç—Ä—ã –≤ —Å—Ç—Ä–æ–∫—É –µ—Å–ª–∏ —ç—Ç–æ –Ω–µ –ø–µ—Ä–≤—ã–π —Ä–µ–Ω–¥–µ—Ä
	useEffect(() => {
		if (isMounted.current) {
			const queryString = qs.stringify({
				sortProperty: sort.sortProperty,
				categoryId,
				currentPage,
			});
			navigate(`?${queryString}`);
		}
		isMounted.current = true;
	}, [sort.sortProperty, categoryId, currentPage, navigate]);

	return (
		<>
			<div className="content__top">
				<Categories />
				<Sort />
			</div>
			<h2 className="content__title">–ü–∏—Ü—Ü—ã</h2>
			<div className="content__items">
				{(status === 'loading' &&
					[...new Array(6)].map((_, i) => <Skeleton key={i} />)) ||
					(status === 'error' && (
						<div className="content__error-info">
							<h2>–ü—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞ üòï</h2>
							<p>
								–ö —Å–æ–∂–∞–ª–µ–Ω–∏—é, –Ω–µ —É–¥–∞–ª–æ—Å—å –ø–æ–ª—É—á–∏—Ç—å –ø–∏—Ü—Ü—ã. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ –ø–æ–≤—Ç–æ—Ä–∏—Ç—å
								–ø–æ–ø—ã—Ç–∫—É –ø–æ–∑–∂–µ.
							</p>
						</div>
					)) ||
					(status === 'success' &&
						items
							.slice(0, itemsPerPage)
							.map((pizza: any) => <PizzaBlock key={pizza.id} {...pizza} />))}
			</div>
			<Pagination />
		</>
	);
};

export default Home;
